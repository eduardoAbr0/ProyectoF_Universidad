    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        
            try {
                donadorEvento = new DonadorEvento(
                    Integer.parseInt(idDonador.getValue().toString()),
                    Integer.parseInt(idEvento.getValue().toString()),
                    Double.parseDouble(donativo.getValue().toString())
                );

                h = new hilos("insertarDonadorEvento");
                h.setObjeto(donadorEvento);
                h.start();
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "Ingrese datos correctos.", "Advertencia", JOptionPane.WARNING_MESSAGE);
            }
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        if (Integer.parseInt(id.getValue().toString()) > 0) {
            try {

                donadorEvento = new DonadorEvento(
                    Integer.parseInt(id.getValue().toString()),
                    Integer.parseInt(idDonador.getValue().toString()),
                    Integer.parseInt(idEvento.getValue().toString()),
                    Double.parseDouble(donativo.getValue().toString())
                );

                h = new hilos("cambiarDonadorEvento");
                h.setObjeto(donadorEvento);
                h.start();
            } catch (NumberFormatException | NullPointerException e) {
                JOptionPane.showMessageDialog(null, "Ingrese datos correctos.", "Advertencia", JOptionPane.WARNING_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "No hay donativo por cambiar.", "Advertencia", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        tableModel.setRowCount(0);

        if (Integer.parseInt(id.getValue().toString()) > 0) {

            h = new hilos("consultarDonadorEvento");
            h.setId(Integer.parseInt(id.getValue().toString()));
            h.start();
            try {
                h.join();
                donadorEvento = (DonadorEvento) h.getObjeto();
            } catch (InterruptedException b) {
                b.printStackTrace();
            }

            if (donadorEvento != null) {
                tableModel.addRow(new Object[]{donadorEvento.getId(),donadorEvento.getIdDonador(),donadorEvento.getIdEvento(),donadorEvento.getDonativo()});
        }
        } else {
            JOptionPane.showMessageDialog(null, "No hay donativo", "Advertencia", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed


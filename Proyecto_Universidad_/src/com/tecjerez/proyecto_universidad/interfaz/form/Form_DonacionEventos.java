    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        tableModel.setRowCount(0);

        h = new hilos("consultarTDonadorEvento");
        h.start();
        try {
            h.join();
            donadorEventos = (ArrayList<DonadorEvento>) h.getObjeto();
        } catch (InterruptedException b) {
            b.printStackTrace();
        }
        if(!donadorEventos.isEmpty()){
            for (DonadorEvento a : donadorEventos) {
                tableModel.addRow(new Object[]{a.getId(),a.getIdDonador(),a.getIdEvento(),a.getDonativo()});
        }
        }else{
            JOptionPane.showMessageDialog(null, "No hay donativos registrados", "Advertencia", JOptionPane.WARNING_MESSAGE);
        }

    }//GEN-LAST:event_jButton2ActionPerformed


